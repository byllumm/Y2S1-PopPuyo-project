public class Position {
    private int x, y;

    public Position() {
        this.x = 0;
        this.y = 0;
    }

    public Position(int x_, int y_) {
        this.x = x_;
        this.y = y_;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
